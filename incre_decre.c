// Increment and Decrement
// ++ --

// Post Decrement = angka++, Pre Decrement = ++angka

#include <stdio.h>
int main() {
    int angka = 27;
    printf("Angka sebelum -- adalah %d\n", angka--); 
    printf("Angka setelah -- adalah %d\n", angka--); 
    // printf("Angka setelah -- adalah %d\n", --angka); 
 
    printf("Angka sebelum ++ adalah %d\n", angka++); 
    printf("Angka setelah ++ adalah %d\n", angka++); 
    return -1;   
}

